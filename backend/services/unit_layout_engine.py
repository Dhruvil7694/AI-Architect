"""
services/unit_layout_engine.py
-------------------------------
Deterministic room layout engine for a single residential unit.

Replaces GPT for internal room geometry.  All coordinates are generated by
proportion calculation using GDCR minimums as hard lower bounds.

── Layout strategy ───────────────────────────────────────────────────────────
Rooms are organised in four horizontal strips (zones), stacked along the
depth axis (Y).  For a SOUTH unit (Y=unit_y = exterior south face):

    Zone A  [exterior]  Bedrooms (all on exterior wall, get south windows)
    Zone B  [service]   Bathrooms + Utility (interior, no windows required)
    Zone C  [public]    Living + Dining + Kitchen
    Zone D  [entry]     Foyer  (corridor-facing strip, width-limited)

For a NORTH unit (Y=unit_y = corridor face) zones are mirrored top-to-bottom.

── Balcony ───────────────────────────────────────────────────────────────────
The balcony projects OUTSIDE the unit rectangle from the exterior face.
It is returned as a separate "balcony" dict (not inside "rooms"), consistent
with the existing GeoJSON converter expectations.

── GDCR compliance ───────────────────────────────────────────────────────────
• All room widths/depths are ≥ GDCR SS 13.1.8 / SS 13.1.9 minimums.
• Living ≥ 9.5 m²,  Master Bed ≥ 9.5 m²,  Bedroom ≥ 7.5 m²
• Kitchen ≥ 5.5 m², Bathroom ≥ 2.16 m²,   Toilet ≥ 1.65 m²
"""

